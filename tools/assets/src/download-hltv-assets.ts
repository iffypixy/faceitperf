const root = fileURLToPath(new URL("../../..", import.meta.url));
const flagsDir = join(root, "apps/web/public/hltv/flags");
const mapsDir = join(root, "apps/web/public/hltv/maps");

mkdirSync(flagsDir, { recursive: true });
mkdirSync(mapsDir, { recursive: true });

const browser = await chromium.launch();
const page = await browser.newPage();

await page.goto("https://www.hltv.org", { waitUntil: "load", timeout: 60000 });

async function download(url: string, dest: string) {
	const res = await page.goto(url, { waitUntil: "load" });
	if (!res?.ok()) return console.warn(`SKIP: ${url} (${res?.status() ?? "N/A"})`);

	writeFileSync(dest, await res.body());
	console.log(`OK: ${dest}`);
}

console.log("Downloading flags…");
for (const code of COUNTRY_CODES)
	await download(
		`https://www.hltv.org/img/static/flags/30x20/${code}.gif`,
		join(flagsDir, `${code}.gif`),
	);

console.log("Downloading maps…");
for (const map of MAPS)
	await download(`https://www.hltv.org/img/static/maps/${map}.png`, join(mapsDir, `${map}.png`));

await browser.close();
console.log("Done.");
